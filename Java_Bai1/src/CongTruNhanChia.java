
public class CongTruNhanChia {

	public static void main(String[] args) {
		System.out.println("10 + 2 = " + (10+2));
		System.out.println("10 - 2 = " + (10-2));
		System.out.println("10 * 2 = " + (10*2));
		System.out.println("10 / 2 = " + (10/2));

		System.out.println("10 + 2 / 6 = " + (10+2/6));
		System.out.println("10 + 2 / 6 = " + (10+2.0/6));
	}

}
